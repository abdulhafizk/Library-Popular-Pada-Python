testString = "Abdulhafizh"

# Ubah setiap huruf dalam string menjadi huruf kapital.
print(testString.upper())

# Ubah setiap huruf dalam string menjadi huruf kecil.
print(testString.lower())

# Pisahkan teks berdasarkan delimiter (karakter pemisah).
print(testString.split("i"))

# Jadikan setiap awal kata kapital.
print(testString.title())

# Tambahkan nol di awal string sebanyak nilai yang ada pada parameter.
print(testString.zfill(20))